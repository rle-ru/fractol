#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	while (n > 7)
	{
		*(uint64_t*)(s) = (uint64_t)0;
		n -= 8;
		s += 8;
	}
	if (n > 3)
	{
		*(uint32_t*)(s) = (uint32_t)0;
		n -= 4;
		s += 4;
	}
	if (n > 1)
	{
		*(uint16_t*)(s) = (uint16_t)0;
		n -= 2;
		s += 2;
	}
	if (n == 1)
		*(uint8_t*)s = 0;
}
